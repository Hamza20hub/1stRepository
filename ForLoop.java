package javaLearning;

public class ForLoop {
	
	

	public static void main(String[] args) {

		for (int i = 0; i < 10; i++) {
			System.out.println("this is a ForLoop");
		}
		for (int i = 0; i <= 7; i++) {
			System.out.println(i);
		}
		for (int i = 0; i < 5; i++) {
			System.out.println("ForLoop excersice");
		}
		for (int i = 0; i < 5; i++) {
			System.out.println("Do ForLoop five time!!");

		}

		for (int i = 0; i <= 7; i++)
			System.out.println(i + " " + "keep trying");
	}

}
