package javaLearning;

public class DoWhileLoop {

	public static void main(String[] args) {
		int a = 0;
		int b = 0;
		int c = 0;
		int d = 0;
		int e = 0;

		do {
			System.out.println("this is a DoWhileLoop ");
			a++;
		}

		while (a <= 4);

		do {
			System.out.println("Learning DoWhileLoop");
			b++;
		} while (b <= 5);

		do {
			System.out.println("DowhileLoop Excersice");
			c++;
		} while (c < 5);

		do {
			System.out.println(d);
			d++;
		} while (d < 7);

		do {
			System.out.println("do DoWhileLoop Five times" + e);
			e++;
		} while (e < 5);

	}

}
