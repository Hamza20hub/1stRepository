package javaLearning;

public class WhileLoops {

	public static void main(String[] args) {

		// While Loop
		int a = 0; // is there any way to use same int value for every whileLoop?
		while (a < 8) {
			System.out.println("This is a WhileLoop");
			a++;

		}
		int b = 0;
		while (b <= 5) {
			System.out.println("Learning whileLoop");
			b++;
		}
		int c = 0;
		while (c <= 3) {
			System.out.println(c + ".WhileLoop Excersice");
			c++;
		}
		int d = 0;
		while (d < 6) {
			System.out.println(d);
			d++;
		}
		int e = 0;
		while (e < 4) {
			System.out.println("Do WhileLoop Five times" + e);
			e++;
		}

	}

}
